%--------------------------------------------------------------------------

% 初始化

%--------------------------------------------------------------------------

clear;clc;

%--------------------------------------------------------------------------

% 参数设置

%--------------------------------------------------------------------------

N = 10000;

E = 0;

I = 1;

S = N - I;

R = 0;

r = 20;

B = 0.03;

a = 0.1;

y = 0.1;

T = 1:150;

for i = 1:length(T)-1

S(i+1) = S(i) - r*B*S(i)*I(i)/N(1);

E(i+1) = E(i) + r*B*S(i)*I(i)/N(1)-a*E(i);

I(i+1) = I(i) + a*E(i) - y*I(i);

R(i+1) = R(i) + y*I(i);

end

plot(T,S,T,E,T,I,T,R);grid on;

xlabel('天');ylabel('人数')

legend('易感者','潜伏者','传染者','康复者')